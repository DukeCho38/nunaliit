package ca.carleton.gcrc.couch.export.csv;

public class CSVColumn {
	private String label;
	private String name;

	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
